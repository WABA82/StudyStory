package kr.co.studystory.domain;

public class DetailQuestion {
	
	private String subject, category, input_date, answer_flag, content, answer_content;

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getInput_date() {
		return input_date;
	}

	public void setInput_date(String input_date) {
		this.input_date = input_date;
	}

	public String getAnswer_flag() {
		return answer_flag;
	}

	public void setAnswer_flag(String answer_flag) {
		this.answer_flag = answer_flag;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getAnswer_content() {
		return answer_content;
	}

	public void setAnswer_content(String answer_content) {
		this.answer_content = answer_content;
	}
}
