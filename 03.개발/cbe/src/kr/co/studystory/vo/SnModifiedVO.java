package kr.co.studystory.vo;

public class SnModifiedVO {
	private String sn_num, subject, meeting_info, content, addr;

	public String getSn_num() {
		return sn_num;
	}

	public void setSn_num(String sn_num) {
		this.sn_num = sn_num;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getMeeting_info() {
		return meeting_info;
	}

	public void setMeeting_info(String meeting_info) {
		this.meeting_info = meeting_info;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}
}
