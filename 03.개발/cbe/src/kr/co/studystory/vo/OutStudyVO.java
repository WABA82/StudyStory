package kr.co.studystory.vo;

public class OutStudyVO {

	private String s_num, id;
	
	public OutStudyVO(String s_num, String id) {
		this.s_num = s_num;
		this.id = id;
	}

	public String getS_num() {
		return s_num;
	}

	public void setS_num(String s_num) {
		this.s_num = s_num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}
