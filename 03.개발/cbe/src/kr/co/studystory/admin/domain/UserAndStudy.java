package kr.co.studystory.admin.domain;

public class UserAndStudy {
	private int weekUser, weekStudy, allUser, allStudy;

	public int getWeekUser() {
		return weekUser;
	}

	public void setWeekUser(int weekUser) {
		this.weekUser = weekUser;
	}

	public int getWeekStudy() {
		return weekStudy;
	}

	public void setWeekStudy(int weekStudy) {
		this.weekStudy = weekStudy;
	}

	public int getAllUser() {
		return allUser;
	}

	public void setAllUser(int allUser) {
		this.allUser = allUser;
	}

	public int getAllStudy() {
		return allStudy;
	}

	public void setAllStudy(int allStudy) {
		this.allStudy = allStudy;
	}
	
	
}
